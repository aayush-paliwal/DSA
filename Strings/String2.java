package Java.Strings;

public class String2 {
    public static void main(String[] args) {
//        Pretty Printing in Java

        float a=125.2345f;
        System.out.printf("Number with two decimal digit is %.2f ",a);
        System.out.println();

        System.out.printf("Value of pie is: %.3f ",Math.PI);
        System.out.println();

        System.out.printf("Hello my name is %s and I am %s","Aayush","good");



//       There are many format specifiers we can use. Here are some common ones:
//
//                %c - Character
//                %d - Decimal number (base 10)
//                %e - Exponential floating-point number
//                %f - Floating-point number
//                %i - Integer (base 10)
//                %o - Octal number (base 8)
//                %s - String
//                %u - Unsigned decimal (integer) number
//                %x - Hexadecimal number (base 16)
//                %t - Date/time
//                %n - Newline
    }
}
